# for loop
print("=========for loop with initial and final=========")
for i in range(5, 10):
    print(i)

print("=========for loop with initial, final and gap=========")
for i in range(0, 10, 2):
    print(i)

print("=========just final=========")
for i in range(5):
    print(i)

print("=========letter=========")
for letter in "Hello":
    print(letter)
    
print("=========list=========")
for i in [5, 4, 3, 2, 1]:
    print(i)
